    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int i = jTable1.getSelectedRow();

        if (i == -1) {
            JOptionPane.showMessageDialog(
                    null, "SELECIONE UMA LINHA!");
        } else {
            String[] opcoes = {"Sim", "Não"};
            int escolha = JOptionPane.showOptionDialog(null, "Tem certeza que quer excluir esse usuario?", "Atenção", 1, 2, null, opcoes, opcoes[0]);
            if (escolha == 0) {
            int c_id = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            System.out.println(jTable1.getSelectedRow());
            System.out.println(c_id);
            try {
                String sql = "delete from customer "
                        + "where customer_id=?";
                PreparedStatement pst = con.prepareStatement(sql);

                pst.setInt(1, c_id);
                pst.execute();
                pst.close();
                System.out.println("Deletado");
            } catch (SQLException ex) {
                Logger.getLogger(SakilaUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            viewTable();
        }else JOptionPane.showMessageDialog(null,"Operação cancelada");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

